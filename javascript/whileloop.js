let abc="";
let i=0;
while(i<5){
  let k=0;
  while(k<i){
    abc+=" ";
    k++;
  }
  let j=0;
while(j<2*(5-i)-1){
  if(j==0||i==0||j==2*(5-i)-1-1){
    abc+="*";
  }else{
abc+=" ";
  }
  j++;
}
abc+="\n"
i++;
}

